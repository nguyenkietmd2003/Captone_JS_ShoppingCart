function ListPhone(
  id,
  name,
  price,
  screen,
  blackcamera,
  frontcamera,
  img,
  desc,
  type
) {
  this.id = id;
  this.name = name;
  this.price = price;
  this.screen = screen;
  this.blackcamera = blackcamera;
  this.frontcamera = frontcamera;
  this.img = img;
  this.desc = desc;
  this.type = type;
}
